# Print string with double quotes
print('"MySirG"')

# Print string with escaped single quote
print('"Teacher\'s Day"')

# Print backslash and n literally
print("\\n")

# Print Windows file path
print('C:\\Users\\John\\Desktop\\File.txt')

# Print string with double quotes
print('"hello babe"')

# Print string with escaped quotes
print('He said,\"Hello\"')

# Print string with single quotes inside double quotes
print("""She said,'it's cold'""")

# Print integer
a=46
print(a)

# Print string variable
name='Roshani'
print(name)

# Print multiple integers on separate lines
a,b,c=20,50,63
print(a,"\n",b,'\n',c,sep="")

